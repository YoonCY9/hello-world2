package loop;

public class loopEx1 {

    public static void main(String[] args) {
        int i = 0;
        while (i <= 10) {
            System.out.println("i의 값: " + i);
            i = i + 1;
        }
        System.out.println("---------");

        for (int j = 0; j <= 10; j++) {
            System.out.println("j의 값: " + j);
        }

    }
}


